
public class OperatorEX03 {

	public static void main(String[] args) {
		
		/*
		 * 증가, 감소 연산자
		 * ++ 1증가
		 * -- 1감소
		 */
		
		int num = 5;
		
		System.out.println("num >> " + num);
		
		//num = num +1; // 1증가 식
		System.out.println("num >> " + num);
		
		num++; // 1증가 연산자
		System.out.println("num >> " + num);
		
		
		

	}

}
