package p230118;

public class TypeEx01 {
	
	public static void main (String[] args) {
		
		//[변수 선언과 동시에 초기화]
		byte one = 127;
 		System.out.println("one :" + one);
 		
 		//접미사 : 정수형 - L(눈에 띄게 대문자 사용)
 		long two = 10000000000L;
 		System.out.println("two :" + two);
 		
 		char three = 5;
 		System.out.println("three : " + three);
	}

}
